import * as core from '@actions/core';
import * as crypto from 'crypto';

// Build a direct link to a specific file in the PR diff
const buildFileLink = (repoUrl, prNumber, filePath) => {
    // If repo url or pr number not passed, empty string as can't construct
    if (!repoUrl || !prNumber) return '';

    // Generate hash of the file path to link to the specific file
    const hash = crypto.createHash('sha256').update(filePath).digest('hex');

    // Return markdown link format with full URL to the file in the PR diff
    return `[View file in Files Changed](${repoUrl}/pull/${prNumber}/files#diff-${hash})`;
};

// Helper function to construct body of PR review
// This one is used when tests are not run
export const buildNewFileReviewBody = (filePath, mcpSummary = '', rawOutput = '', repoUrl = '', prNumber = '') => {
    // Escape backticks in raw output to prevent breaking the rendered code block
    const escapedRaw = rawOutput.replace(/```/g, '\\`\\`\\`');
    const truncatedRaw =
        escapedRaw.length > 30000 ? escapedRaw.substring(0, 30000) + '\n\n... (truncated)' : escapedRaw;
    const filesLink = buildFileLink(repoUrl, prNumber, filePath);

    // Include heading with status, exit code, file name with link,
    // instructions for keeping or removing the file
    const sections = [
        '### 🤖 AI-Generated Playwright Test',
        '',
        `A test file has been committed to this PR: **\`${filePath}\`** ${filesLink}`,
        '',
        '**To keep it:** Dismiss this review — the file stays on the branch.',
        '**To remove it:** Delete the file from the branch or revert the commit, then dismiss.',
        '',
        '> Tests were not executed. Set `run-tests: true` in the workflow to validate before merging.',
    ];

    // Include raw LLM output for visibility if avaiable
    if (rawOutput) {
        sections.push(
            '',
            `<details>\n<summary>Raw LLM Output</summary>\n\n\`\`\`\n${truncatedRaw}\n\`\`\`\n\n</details>`
        );
    }

    // Include MCP tool call summary if available
    if (mcpSummary) {
        sections.push('', mcpSummary);
    }

    // Add footer note - disclaimer to users that full process is AI generated
    // To ensure users understand the source of the generated content
    sections.push('', '---', '*Generated by AI Playwright Test Generator*');

    // Finally, join it all back using new lines to ensure formatting
    return sections.join('\n');
};

// Helper function to construct body of PR review with test results
export const buildResultsReviewBody = ({
    filePath,
    passed,
    exitCode,
    output,
    mcpSummary = '',
    rawOutput = '',
    testCode = '',
    repoUrl = '',
    prNumber = '',
}) => {
    // Use the test results to determine the status emoji and message
    const status = passed ? '✅ Tests Passed' : '❌ Tests Failed';

    // Truncate outputs if too long
    const truncatedOutput = output.length > 30000 ? output.substring(0, 30000) + '\n\n... (truncated)' : output;

    // Escape backticks in raw output to prevent breaking the rendered code block
    const escapedRaw = rawOutput.replace(/```/g, '\\`\\`\\`');
    const truncatedRaw =
        escapedRaw.length > 30000 ? escapedRaw.substring(0, 30000) + '\n\n... (truncated)' : escapedRaw;

    // Build link to file in PR diff for quicker review
    const filesLink = buildFileLink(repoUrl, prNumber, filePath);

    // Build sections array and join with newlines for clean formatting
    // As previously, formatting was being broken by markdown

    // Include heading with status, exit code, file name with link,
    // instructions for keeping or removing the file,
    // test output, any mcp calls summary if made
    const sections = [
        `### 🤖 AI-Generated Playwright Tests — ${status}`,
        `**Exit code:** ${exitCode}`,
        `**Test file:** \`${filePath}\` ${filesLink}`,
        '',
        passed
            ? '**To keep it:** Dismiss this review — the file stays on the branch.\n**To remove it:** Delete the file from the branch or revert the commit, then dismiss.'
            : '**Tests failed — file was not committed.** The generated code is shown below for reference.',
        '',
        `<details>\n<summary>Generated Test Code</summary>\n\n\`\`\`typescript\n${testCode}\n\`\`\`\n\n</details>`,
        '',
        `<details>\n<summary>Test Output</summary>\n\n\`\`\`\n${truncatedOutput}\n\`\`\`\n\n</details>`,
    ];

    // Include raw LLM output for visibility if avaiable
    if (rawOutput) {
        sections.push(
            '',
            `<details>\n<summary>Raw LLM Output</summary>\n\n\`\`\`\n${truncatedRaw}\n\`\`\`\n\n</details>`
        );
    }

    // Include MCP tool call summary if available
    if (mcpSummary) {
        sections.push('', mcpSummary);
    }

    // Add footer note - disclaimer to users that full process is AI generated
    // To ensure users understand the source of the generated content
    sections.push('', '---', '*Generated and executed by AI Playwright Test Generator*');

    // Finally, join it all back using new lines to ensure formatting
    return sections.join('\n');
};
