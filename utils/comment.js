import * as core from '@actions/core';
import * as crypto from 'crypto';

// Build a direct link to a specific file in the PR diff
const buildFileLink = (repoUrl, prNumber, filePath) => {
    // If repo url or pr number not passed, empty string as can't construct
    if (!repoUrl || !prNumber) return '';

    // Generate hash of the file path to link to the specific file
    const hash = crypto.createHash('sha256').update(filePath).digest('hex');

    // Return markdown link format with full URL to the file in the PR diff
    return `[View file in Files Changed](${repoUrl}/pull/${prNumber}/files#diff-${hash})`;
};

// Helper function to construct body of PR review
// This one is used when tests are not run
export const buildNewFileReviewBody = (filePath, mcpSummary = '', rawOutput = '', repoUrl = '', prNumber = '') => {
    const truncatedRaw = rawOutput.length > 30000 ? rawOutput.substring(0, 30000) + '\n\n... (truncated)' : rawOutput;
    const filesLink = buildFileLink(repoUrl, prNumber, filePath);

    return `### 🤖 AI-Generated Playwright Test

A test file has been committed to this PR: **\`${filePath}\`** ${filesLink}

**To keep it:** Dismiss this review — the file stays on the branch.
**To remove it:** Delete the file from the branch or revert the commit, then dismiss.

> Tests were not executed. Set \`run-tests: true\` in the workflow to validate before merging.
${
    rawOutput
        ? `
<details>
<summary>Raw LLM Output</summary>

\`\`\`
${truncatedRaw}
\`\`\`

</details>
`
        : ''
}
${mcpSummary ? `${mcpSummary}\n` : ''}
---
*Generated by AI Playwright Test Generator*`;
};

// Helper function to construct body of PR review with test results
export const buildResultsReviewBody = ({
    filePath,
    passed,
    exitCode,
    output,
    mcpSummary = '',
    rawOutput = '',
    testCode = '',
    repoUrl = '',
    prNumber = '',
}) => {
    // Use the test results to determine the status emoji and message
    const status = passed ? '✅ Tests Passed' : '❌ Tests Failed';

    // Truncate outputs if too long
    const truncatedOutput = output.length > 30000 ? output.substring(0, 30000) + '\n\n... (truncated)' : output;
    const truncatedRaw = rawOutput.length > 30000 ? rawOutput.substring(0, 30000) + '\n\n... (truncated)' : rawOutput;

    // Build link to file in PR diff for quicker review
    const filesLink = buildFileLink(repoUrl, prNumber, filePath);

    // Return formatted review body with test results and output
    return `### 🤖 AI-Generated Playwright Tests — ${status}

**Exit code:** ${exitCode}
**Test file:** \`${filePath}\` ${filesLink}

${passed ? '**To keep it:** Dismiss this review — the file stays on the branch.\n**To remove it:** Delete the file from the branch or revert the commit, then dismiss.' : '**Tests failed — file was not committed.** The generated code is shown below for reference.'}

<details>
<summary>Generated Test Code</summary>

\`\`\`typescript
${testCode}
\`\`\`

</details>

<details>
<summary>Test Output</summary>

\`\`\`
${truncatedOutput}
\`\`\`

</details>
${
    rawOutput
        ? `
<details>
<summary>Raw LLM Output</summary>

\`\`\`
${truncatedRaw}
\`\`\`

</details>
`
        : ''
}
${mcpSummary ? `${mcpSummary}\n` : ''}
---
*Generated and executed by AI Playwright Test Generator*`;
};
